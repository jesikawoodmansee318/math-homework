public class MathHomework {
    public static void main(String[] args) {
        int sum = 0;
        int product = 1;
        
        // Example: Sum of first n natural numbers (n is a positive integer)
        for (int i = 1; i <= 5; i++) {
            sum += i;
        }
        
        // Example: Product of first n natural numbers
        for (int i = 1; i <= 5; i++) {
            product *= i;
        }
    }
}
