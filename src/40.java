public class MathHomework {
    public static void main(String[] args) {
        // Example 1: Simple arithmetic operations
        int result1 = 5 + 3;
        System.out.println("Result of 5 + 3: " + result1);

        // Example 2: Basic mathematical expressions
        double result2 = 7 * 4 - 2 / 0.5 + 3.14;
        System.out.println("Result of 7 * 4 - 2 / 0.5 + 3.14: " + result2);

        // Example 3: Simple mathematical functions
        double squareRoot = Math.sqrt(9);
        System.out.println("Square root of 9: " + squareRoot);

        // Example 4: Comparison and logical operations
        boolean isEqual = true;
        if (isEqual) {
            System.out.println("Equal values found");
        } else {
            System.out.println("Not equal values found");
        }

        // Example 5: Input/output operations
        String input1 = "Hello";
        System.out.println("Input1 is: " + input1);

        String output = "World!";
        System.out.println("Output: " + output);
    }
}
