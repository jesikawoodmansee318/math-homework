public class MathSolver {
    public static void main(String[] args) {
        // Example of solving a simple arithmetic problem: 2 + 3 = 5
        int result = 2 + 3;
        System.out.println("The solution is " + result);
        
        // Another example using a loop to iterate through a range
        for (int i = 0; i < 10; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
