public class MathHomework {
    public static void main(String[] args) {
        // Example of using variables in Java
        int x = 5;
        double y = 10.5;
        
        // Accessing local variable from within a method
        System.out.println("Local Variable: " + x);
        
        // Calculating the sum of two integers
        int result = x + y;
        System.out.println("Sum of " + x + " and " + y + ": " + result);
    }
}
